# Валидационная выборка
x_val = [1.5, 2.5, 3.5]
y_val = [0, 0, 0]

FNN_x = [(x - 1)*(x - 2)*(x - 3)*(x - 4) for x in x_val]

# MAE - Сумма модулей разности предсказанных и фактических значений, разделенная
# на количество объектов в валидационной выборке
MAE = sum([abs(fnn - y) for fnn, y in zip(FNN_x, y_val)]) / len(x_val)
print(MAE)
